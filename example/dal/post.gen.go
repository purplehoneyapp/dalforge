func (d *postRepository) createBulk(ctx context.Context, entities []*Post) ([]*Post, error) {
    const operation = "create_bulk"
    start := time.Now()

    if len(entities) == 0 {
        return nil, fmt.Errorf("empty entities list")
    }

    // Get the database connection once, outside the loop
    db, dbErr := d.dbProvider.GetDatabase("post", true)
    if dbErr != nil {
        d.telemetryProvider.IncDBError("post", operation)
        return nil, dbErr
    }

    now := time.Now().Truncate(time.Second)
    batchSize := 500 // Safe limit to prevent exceeding MySQL's max placeholders

    // Process entities in chunks
    for i := 0; i < len(entities); i += batchSize {
        end := i + batchSize
        if end > len(entities) {
            end = len(entities)
        }
        
        chunk := entities[i:end]

        // Prepare data and parameters scoped ONLY to this chunk
        valuePlaceholders := make([]string, 0, len(chunk))
        // Dynamically calculate fields per entity: custom columns + 2 (created, updated)
        params := make([]interface{}, 0, len(chunk)*(8 + 2)) 

        for _, entity := range chunk {
            if entity.ID > 0 {
                return nil, fmt.Errorf("entity with existing ID in bulk create")
            }

            entity.Created = now
            entity.Updated = now

			// Auto-generate UID fields if they are not provided

            valuePlaceholders = append(valuePlaceholders, "(?,?,?,?,?,?,?,?,?,?)")
            params = append(params,
                entity.Deleted,
                entity.ExpiresAt,
                entity.LanguageId,
                entity.Post,
                entity.Revoked,
                entity.StoryUid,
                entity.TargetAge,
                entity.UserId,
                entity.Created,
                entity.Updated,
            )
        }

        query := fmt.Sprintf(`
            INSERT INTO posts
            (deleted,expires_at,language_id,post,revoked,story_uid,target_age,user_id,
created,updated)
            VALUES %s
        `, strings.Join(valuePlaceholders, ","))

        result, err := db.ExecContext(ctx, query, params...)
        if err != nil {
            d.telemetryProvider.IncDBError("post", operation)
            return nil, fmt.Errorf("failed to bulk insert posts chunk: %w", err)
        }

        // Get generated IDs (works for MySQL/SQLite)
        lastID, err := result.LastInsertId()
        if err != nil {
            return nil, fmt.Errorf("failed to get last insert ID for chunk: %w", err)
        }

        rowsAffected, err := result.RowsAffected()
        if err != nil {
            return nil, fmt.Errorf("failed to get rows affected for chunk: %w", err)
        }

        if rowsAffected != int64(len(chunk)) {
            return nil, fmt.Errorf("mismatch between inserted rows and entity count in chunk")
        }

        // Assign generated IDs sequentially to the current chunk
        for j := range chunk {
            chunk[j].ID = lastID + int64(j)
			d.setCached(chunk[j])
        }
    }

    d.telemetryProvider.IncDBRequest("post", operation) 
    d.telemetryProvider.ObserveDBLatency("post", operation, time.Since(start).Seconds())    
    return entities, nil
}



